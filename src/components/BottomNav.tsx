export function BottomNav() {
  const pathname = usePathname();
  const [mounted, setMounted] = useState(false);
  
  // Only show after mount to avoid hydration mismatch
  useEffect(() => {
    setMounted(true);
  }, []);
  
  if (!mounted) return null;
  
  const isActive = (href: string) => {
    if (href === '/') {
      return pathname === '/';
    }
    return pathname.startsWith(href);
  };
  
  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-[#2D2D2D] text-white border-t border-[#383838] z-50">
      <div className="grid grid-cols-4 max-w-3xl mx-auto">
        {navItems.map((item) => {
          const active = isActive(item.href);
          
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center py-2 text-xs font-medium transition-colors ${
                active 
                  ? 'text-[#FC2B4E]' 
                  : 'text-gray-400 hover:text-white'
              }`}
            >
              {item.icon}
              <span className="mt-1">{item.label}</span>
              
              {/* Indicator dot */}
              <div className="h-1 w-1 rounded-full mt-1">
                {active && <div className="h-1 w-1 rounded-full bg-[#FC2B4E]" />}
              </div>
            </Link>
          );
        })}
      </div>
      
      {/* Safe area padding for notched devices */}
      <div className="h-safe-bottom bg-[#2D2D2D]" />
    </nav>
  );
} 